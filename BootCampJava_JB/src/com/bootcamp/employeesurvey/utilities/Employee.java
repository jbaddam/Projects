package com.bootcamp.employeesurvey.utilities;

/**
 * @author Jagan Reddy
 * employee information entity class
 */
public class Employee{
	
	private String experience;
	private String j2eeTechnolgy;
	private String webservice;
	private String framework;
	private String comments;

	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}

	public String getExperience() {
		return experience;
	}

	public void setExperience(String experience) {
		this.experience = experience;
	}

	public String getJ2eeTechnolgy() {
		return j2eeTechnolgy;
	}

	public void setJ2eeTechnolgy(String strings) {
		this.j2eeTechnolgy = strings;
	}

	public String getWebservice() {
		return webservice;
	}

	public void setWebservice(String webservice) {
		this.webservice = webservice;
	}

	public String getFramework() {
		return framework;
	}

	public void setFramework(String frameworks) {
		this.framework = frameworks;
	}

	

}
